#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  auto work = [&]() {
    std::vector<int> f(n + 1, LLONG_MIN / 3), g(n + 1, LLONG_MIN / 3);
    // f[0] = g[0] = 0;
    for (int i = 1; i <= n; i++) {
      f[i] = std::max(a[i], f[i - 1] + a[i]);
      g[i] = std::max(f[i], g[i - 1]);
    }
    return g;
  };
  auto g = work();
  std::reverse(a.begin() + 1, a.end());
  auto rg = work();
  std::reverse(rg.begin() + 1, rg.end());
  std::reverse(a.begin() + 1, a.end());
  int ans = LLONG_MIN / 3;
  for (int i = 3; i <= n - 2; i++) {
    ans = std::max(ans, a[i] + g[i - 2] + rg[i + 2]);
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
