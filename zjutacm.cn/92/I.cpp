#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, m;
  std::cin >> n >> m;
  std::vector<std::vector<int>> a(n, std::vector<int>(m));
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < m; j++) {
      std::cin >> a[i][j];
    }
  }
  std::vector<std::pair<int, int>> ans;
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < m; j++) {
      int x;
      std::cin >> x;
      if (a[i][j] == 1 && x == 0) {
        ans.push_back({i, j});
      }
    }
  }
  std::cout << ans.size() << '\n';
  for (auto x : ans) {
    std::cout << x.first + 1 << ' ' << x.second + 1 << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
