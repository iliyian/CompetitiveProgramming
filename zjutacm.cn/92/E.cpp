#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  int f[2] {};
  for (auto &i : a) std::cin >> i, f[i % 2] |= 1;
  if (f[0] && f[1]) {
    std::cout << "Yes\n";
  } else if (std::is_sorted(a.begin(), a.end()) || std::is_sorted(a.rbegin(), a.rend())) {
    std::cout << "Yes\n";
  } else {
    std::cout << "No\n";
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
