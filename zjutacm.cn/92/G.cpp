#include <bits/stdc++.h>
#define int long long

void solve() {
  int ans = 12345679;
  for (int i = 1, j = 0; j < 9; i += 9, j++) {
    std::vector<int> v;
    int t = ans * i;
    for (int k = 0; k < 9; k++) {
      v.push_back(t % 10);
      t /= 10;
    }
    std::reverse(v.begin(), v.end());
    for (auto i : v) {
      std::cout << i << ' ';
    }
    std::cout << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
