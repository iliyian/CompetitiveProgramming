#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<std::pair<int, int>> ans;
  for (int x = 1; x * (x + 1) <= n; x++) {
    int s = x;
    for (int y = x + 1; s * y <= n; y++) {
      s *= y;
      if (s == n) {
        ans.push_back({x, y});
        break;
      }
    }
  }
  std::cout << ans.size() << '\n';
  for (auto x : ans) {
    for (int i = x.first; i <= x.second; i++) {
      std::cout << i << " \n"[i == x.second];
    }
    // std::cout << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
