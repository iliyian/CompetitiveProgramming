#include <bits/stdc++.h>
#define int long long

int qpow(int a, int b, int p) {
  int ans = 1;
  while (b) {
    if (b & 1) ans = ans * a % p;
    a = a * a % p;
    b >>= 1;
  }
  return ans;
}

void solve() {
  int n, d, p;
  std::cin >> n >> d >> p;
  int s = 0;
  while (n--) {
    int x;
    std::cin >> x;
    s += x;
  }
  std::cout << s % p * qpow(d, p - 2, p) % p << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
