#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<std::pair<int, int>> a(n);
  for (auto &[x, y] : a) {
    std::cin >> x >> y;
  }
  int x0;
  std::cin >> x0;
  int ansy = LLONG_MAX, ans = 0;
  for (int i = 0; i < n; i++) {
    int j = (i + 1) % n;
    if ((a[i].first - x0) * (a[j].first - x0) < 0 && std::max(a[j].second, a[i].second) > 0) {
      if (std::min(a[i].second, a[j].second) < ansy) {
        ans = i;
        ansy = std::min(a[i].second, a[j].second);
      }
    }
  }
  std::cout << ans + 1 << ' ' << (ans + 1) % n + 1 << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
