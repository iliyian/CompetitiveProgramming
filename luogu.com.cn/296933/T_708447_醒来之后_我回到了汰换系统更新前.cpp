#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, m;
  std::cin >> n >> m;
  std::vector<int> v(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> v[i];
  }
  std::vector<std::vector<int>> w(m + 1, std::vector<int>(n + 1));
  for (int i = 1; i <= m; i++) {
    for (int j = 1; j <= n; j++) {
      std::cin >> w[i][j];
    }
  }
  int ans = 0;
  for (int j = 1; j <= n; j++) {
    int mx = 0;
    for (int i = 1; i <= m; i++) {
      mx = std::max(mx, w[i][j] - v[j]);
    }
    ans += mx;
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
