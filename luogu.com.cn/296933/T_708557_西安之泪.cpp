#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<std::vector<int>> g(n + 1);
  for (int i = 2; i <= n; i++) {
    int x, y;
    std::cin >> x >> y;
    g[x].push_back(y);
    g[y].push_back(x);
  }
  std::vector<std::array<int, 2>> ans;
  for (int u = 1; u <= n; u++) {
    for (int v : g[u]) {
      ans.push_back({v, u});
    }
    if (g[u].size() % 2 == 0) {
      ans.push_back({u, u});
    }
  }
  std::cout << ans.size() << '\n';
  for (auto [r, u] : ans) {
    std::cout << r << ' ' << u << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
