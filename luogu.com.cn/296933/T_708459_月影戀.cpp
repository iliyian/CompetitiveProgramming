#include <bits/stdc++.h>
#define int long long

int hide = 10000000000;

int ask(int a, int b) {
  std::cout << "? " << a << ' ' << b << std::endl;
  int ans;
  // std::cin >> ans;
  ans = std::gcd(a, b + hide);
  return ans;
}

void solve() {
  int r = 0;
  long long k = 1ll << 60;
  while (1) {
    int g = ask(k, k - r);
    if (g == k) {
      std::cout << "! " << r << std::endl;
      return;
    } else {
      r += g;
    }
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
