#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  for (auto &i : a) std::cin >> i;
  if (n == 1) {
    std::cout << 1 << '\n';
    return;
  }
  int target;
  if (a[0] != n) {
    target = n;
  } else {
    target = n - 1;
  }
  int j = 0;
  while (a[j] != target) j++;
  for (int i = j; i < n; i++) {
    std::cout << a[i] << ' ';
  }
  if (j != n - 1) {
    std::cout << a[j - 1] << ' '; 
    j--;
  }
  for (int i = j - 1; i >= 0; i--) {
    if (a[i] >= a[0]) {
      std::cout << a[i] << ' ';
    } else {
      for (int k = 0; k <= i; k++) {
        std::cout << a[k] << ' ';
      }
      break;
    }
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
