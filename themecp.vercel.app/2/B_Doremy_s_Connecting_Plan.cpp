#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, c;
  std::cin >> n >> c;
  std::vector<int> a(n), p(n);
  for (auto &i : a) std::cin >> i;
  std::ranges::iota(p, 0);
  std::ranges::sort(p, [&](auto x, auto y) {
    return (x + 1) * c - a[x] < (y + 1) * c - a[y];
  });
  int sum = a[0];
  for (int i = 0; i < n; i++) {
    if (p[i] == 0) continue;
    if (sum + a[p[i]] < (p[i] + 1) * c) {
      std::cout << "No\n";
      return;
    } else {
      sum += a[p[i]];
    }
  }
  std::cout << "Yes\n";
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
