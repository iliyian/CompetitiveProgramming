#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  int c0 = 0, c1 = 0;
  for (int i = 0; i < n; i++) {
    int x;
    std::cin >> x;
    if (x % 2 == 0) {
      c0++;
    } else {
      c1++;
    }
  }
  if ((c1 + 1) / 2 % 2 == 0) {
    std::cout << "Alice\n";
  } else if (c0 % 2 == 1 && c1 % 2 == 1) {
    std::cout << "Alice\n";
  } else {
    std::cout << "Bob\n";
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
