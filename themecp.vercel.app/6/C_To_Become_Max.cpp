#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, k;
  std::cin >> n >> k;
  std::vector<int> a(n + 2), pre(n + 1);
  int ans = 0;
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    pre[i] = pre[i - 1] + a[i];
  }
  for (int l = 1; l <= n; l++) {
    for (int r = l; r <= n; r++) {
      int len = r - l + 1;
      int _l = a[r], _r = std::max(a[r], a[r + 1] + 1), _ans = -1;
      while (_l <= _r) {
        int mid = (_l + _r) / 2;
        int need = (mid + mid + len - 1) * len / 2 - (pre[r] - pre[l - 1]);
        if (need <= k) {
          _l = mid + 1, _ans = mid;
        } else {
          _r = mid - 1;
        }
      }
      if (_ans == -1) {
        continue;
      }
      ans = std::max(ans, _ans + len - 1);
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  for (int i = 1; i <= t; i++) {
    solve();
  }

  return 0;
}
