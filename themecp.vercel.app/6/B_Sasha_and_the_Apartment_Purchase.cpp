#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, k;
  std::cin >> n >> k;
  std::vector<int> a(n);
  for (auto &i : a) std::cin >> i;
  std::ranges::sort(a);
  std::vector<int> b, c;
  for (int i = k; i < n; i++) {
    b.push_back(a[i]);
  }
  int r = b[b.size() / 2];
  for (int i = 0; i < n - k; i++) {
    c.push_back(a[i]);
  }
  int l = c[(c.size() - 1) / 2];
  // std::cerr << l << ' '  << r << '\n';
  std::cout << r - l + 1 << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
