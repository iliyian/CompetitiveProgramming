#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  int x = 1e5;
  for (auto &i : a) std::cin >> i, i = std::abs(i);
  for (int i = 0; i < n; i++) {
    std::swap(a[i], a[n - 1]);
    std::bitset<200001> bs;
    bs[x] = 1;
    for (int j = 0; j < n - 1; j++) {
      bs |= (bs << a[j]) | (bs >> a[j]);
    }
    if (bs[a[n - 1] + x]) {
      std::cout << "YES\n";
      return;
    }
    std::swap(a[i], a[n - 1]);
  }
  std::cout << "NO\n";
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
