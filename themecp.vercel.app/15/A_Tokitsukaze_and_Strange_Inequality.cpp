#include <bits/stdc++.h>
// #define int long long

using i64 = long long;

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  std::vector<std::vector<int>> pre(n + 1, std::vector<int>(n + 1));
  std::vector<std::vector<int>> suf(n + 2, std::vector<int>(n + 2));
  for (int i = 1; i <= n; i++) {
    for (int j = 1; j <= n; j++) {
      pre[i][j] = pre[i - 1][j] + (a[i] <= j);
    }
  }
  for (int i = n; i >= 1; i--) {
    for (int j = 1; j <= n; j++) {
      suf[i][j] = suf[i + 1][j] + (a[i] <= j);
    }
  }
  i64 ans = 0;
  for (int b = 1; b < n; b++) {
    for (int c = b + 1; c < n; c++) {
      ans += i64(1) * pre[b - 1][a[c] - 1] * suf[c + 1][a[b] - 1];
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
