#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, m;
  std::cin >> n >> m;
  std::vector<int> v(n + 1);
  for (int i = 1; i <= m; i++) {
    int x, y;
    std::cin >> x >> y;
    if (x > y) std::swap(x, y);
    v[y] = std::max(v[y], x);
  }
  int mx = 0, ans = 0;
  for (int i = 1; i <= n; i++) {
    mx = std::max(mx, v[i]);
    ans += i - mx;
    // std::cerr << i - mx << '\n';
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
