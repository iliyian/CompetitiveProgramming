#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, k;
  std::cin >> n >> k;
  std::vector<int> ans(n + 1, -1000);
  int i = 0;
  while ((i + 2) * (i + 1) / 2 <= k) {
    ans[i + 1] = 2;
    i++;
  }
  int d = k - i * (i + 1) / 2;
  if (d) {
    ans[i + 1] = -2 * (i - d + 1) + 1;
  }
  for (int i = 1; i <= n; i++) {
    std::cout << ans[i] << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
