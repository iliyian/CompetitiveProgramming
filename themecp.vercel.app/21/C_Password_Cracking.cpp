#include <bits/stdc++.h>
#define int long long

int ask(std::string s) {
  std::cout << "? " << s << std::endl;
  int ans;
  std::cin >> ans;
  return ans;
}

void solve() {
  int n;
  std::cin >> n;
  if (!ask("0")) {
    std::cout << "! " << std::string(n, '1') << std::endl;
    return;
  }
  std::string ans = "0";
  while (1) {
    if (ans.size() == n) break;
    ans += '0';
    if (!ask(ans)) {
      ans.pop_back();
      ans.push_back('1');
      if (!ask(ans)) {
        ans.pop_back();
        break;
      }
    }
  }
  while (ans.size() < n) {
    if (ask("0" + ans)) {
      ans = "0" + ans;
    } else {
      ans = "1" + ans;
    }
  }
  std::cout << "! " << ans << std::endl;
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
