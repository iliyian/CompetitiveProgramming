#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, m, k;
  std::cin >> n >> m >> k;
  std::vector<int> a(n + 1), p(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    p[i] = i;
  }
  int ans = 0;
  std::ranges::sort(p, [&](auto x, auto y) {
    return a[x] < a[y];
  });
  int s = 0;
  for (int i = 1; s < k; i++) {
    int now = std::min(k - s, m);
    ans += (a[p[i]] + s) * now;
    s += now;
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
