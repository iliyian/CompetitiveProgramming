#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<std::vector<int>> a(n);
  std::vector<std::map<int, int>> cnt(n);
  std::vector<int> mex, mex2;
  std::map<int, int> delta;
  int ans = 0, summex = 0;
  for (int i = 0; i < n; i++) {
    auto &v = a[i];
    int l;
    std::cin >> l;
    v.assign(l, 0);
    for (auto &j : v) {
      std::cin >> j;
      cnt[i][j]++;
    }
    int m = 0;
    while (cnt[i][m]) m++;
    mex.push_back(m);
    summex += m;
    m++;
    while (cnt[i][m]) m++;
    mex2.push_back(m);
    delta[mex.back()] += mex2.back() - mex.back();
  }
  for (int i = 0; i < n; i++) {
    for (int v : a[i]) {
      ans += (n - 1) * summex;
      ans += delta[v];
      if (cnt[i][v] == 1 && v < mex[i]) { // 本地降低
        ans += (-mex[i] + v) * (n - 1);
      }
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
