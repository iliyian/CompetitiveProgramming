#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1), ans(n + 1);
  std::set<int> s;
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    s.insert(a[i]);
  }
  a[0] = LLONG_MAX / 3;
  int now = n;
  for (int i = 1; i <= n; i++) {
    int req = a[i] - (n - i);
    if (req < 0 || req > i || a[i] > a[i - 1]) {
      std::cout << "NO\n";
      return;
    }
    while (s.count(now)) now--;
    if (a[i] == a[i - 1] || i == 1) {
      ans[i] = now;
      now--;
    } else {
      ans[i] = ans[i - 1];
    }
  }
  std::cout << "YES\n";
  for (int i = 1; i <= n; i++) {
    std::cout << ans[i] << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}