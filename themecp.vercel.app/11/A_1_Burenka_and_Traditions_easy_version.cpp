#include <bits/stdc++.h>
// #define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  std::vector<std::vector<int>> f(n + 1, std::vector<int>(8192, INT_MAX / 3));
  f[0][0] = 0;
  for (int i = 1; i <= n; i++) {
    f[i][a[i]] = f[i - 1][0];
    for (int j = 0; j < 8192; j++) {
      f[i][j ^ a[i]] = std::min(f[i][j ^ a[i]], f[i - 1][0] + 1);
      f[i][j ^ a[i]] = std::min(f[i][j ^ a[i]], f[i - 1][j] + 1);
    }
  }
  std::cout << f[n][0] << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
