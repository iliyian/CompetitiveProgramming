#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  int k;
  std::cin >> k;
  for (int i = n - 1; i >= 1; i--) {
    a[i] = std::min(a[i], a[i + 1]);
  }
  int mx = LLONG_MAX / 3;
  for (int i = 1; i <= n; i++) {
    int v = a[i] - a[i - 1];
    int t = v == 0 ? mx : std::min(mx, k / v);
    k -= v * t;
    mx = t;
    std::cout << t << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
