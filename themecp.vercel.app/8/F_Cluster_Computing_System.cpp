#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n), pre(n + 1), suf(n + 1);
  for (auto &i : a) std::cin >> i;
  for (int i = 0; i < n; i++) {
    pre[i + 1] = std::gcd(pre[i], a[i]);
  }
  for (int i = n - 1; i >= 0; i--) {
    suf[i] = std::gcd(suf[i + 1], a[i]);
  }
  int ans = 0;
  for (int i = 1; i < n; i++) {
    ans += std::min(pre[i + 1], suf[i]);
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
