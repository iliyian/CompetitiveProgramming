#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, k;
  std::cin >> n >> k;
  std::string s, t;
  std::cin >> s >> t;
  auto ss = s, tt = t;
  std::ranges::sort(ss);
  std::ranges::sort(tt);
  if (ss != tt) {
    std::cout << "NO\n";
    return;
  }
  if (n >= 6) {
    std::cout << "YES\n";
    return;
  }
  for (int i = 0; i < n; i++) {
    if (s[i] != t[i] && i - 3 < 0 && i + 3 >= n) {
      std::cout << "NO\n";
      return;
    }
  }
  std::cout << "YES\n";
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
