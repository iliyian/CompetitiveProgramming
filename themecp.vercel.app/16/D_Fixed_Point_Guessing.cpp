#include <bits/stdc++.h>
#define int long long

int ask(int l, int r) {
  std::cout << "? " << l << ' ' << r << std::endl;
  int ans = (r - l + 1) & 1 ^ 1;
  for (int i = l; i <= r; i++) {
    int x;
    std::cin >> x;
    ans ^= x < l || x > r;
  }
  return ans;
}

void solve() {
  int n;
  std::cin >> n;
  int l = 1, r = n;
  while (l < r) {
    int mid = (l + r) / 2;
    if (ask(l, mid)) l = mid + 1;
    else r = mid;
  }
  std::cout << "! " << l << std::endl;
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
