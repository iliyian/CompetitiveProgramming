#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, x, y;
  std::cin >> n >> x >> y;
  std::string s;
  std::cin >> s;
  std::vector<int> p(n);
  int sx = 0, sy = 0, sum = 0;
  int c[2] {};
  for (int i = 0; i < n; i++) {
    std::cin >> p[i];
    c[s[i] - '0']++;
    sum += p[i];
    if (s[i] == '0') {
      sx += p[i] / 2 + 1;
    } else {
      sy += p[i] / 2 + 1;
    }
  }
  if (x + y >= sum && sx <= x && sy <= y) {
    if (!c[0] && y - x >= n || !c[1] && x - y >= n || c[0] && c[1]) {
      std::cout << "YES\n";
      return;
    }
  }
  std::cout << "NO\n";
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
