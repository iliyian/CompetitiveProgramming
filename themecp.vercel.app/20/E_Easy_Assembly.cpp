#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  int ans = 0;
  std::vector<std::vector<int>> a(n + 1);
  std::vector<int> b;
  for (int i = 1; i <= n; i++) {
    int len;
    std::cin >> len;
    a[i].assign(len, 0);
    for (auto &i : a[i]) {
      std::cin >> i;
      b.push_back(i);
    }
  }
  std::ranges::sort(b);
  b.erase(std::unique(b.begin(), b.end()), b.end());
  for (int i = 1; i <= n; i++) {
    for (int j = 1; j < a[i].size(); j++) {
      auto p = std::ranges::upper_bound(b, a[i][j - 1]);
      if (p == b.end() || *p != a[i][j]) {
        ans++;
      }
    }
  }
  std::cout << ans << ' ' << ans + n - 1 << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
