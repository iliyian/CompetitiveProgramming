#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::string a, b;
  std::cin >> a >> b;
  std::vector<int> p, q;
  int s = 0;
  for (int i = 0; i < n; i++) {
    s += a[i] == '0';
    p.push_back(2 * s - (i + 1));
  }
  s = 0;
  for (int i = 0; i < n; i++) {
    s += b[i] == '0';
    q.push_back((i + 1) - s * 2);
  }
  std::ranges::sort(p);
  std::ranges::sort(q);
  std::vector<int> sq(n + 1);
  for (int i = 0; i < n; i++) {
    sq[i + 1] = sq[i] + q[i];
  }
  int ans = 0;
  for (int i : p) {
    auto pt = std::ranges::lower_bound(q, i) - q.begin();
    ans += pt * i - sq[pt] + sq[n] - sq[pt] - (n - pt) * i;
  }
  std::cout << (n * n * (n + 1) - ans) / 2 << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
