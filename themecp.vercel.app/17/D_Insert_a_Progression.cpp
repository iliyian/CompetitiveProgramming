#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, x;
  std::cin >> n >> x;
  std::vector<int> a(n);
  for (auto &i : a) std::cin >> i;
  auto [mn, mx] = std::ranges::minmax_element(a);
  int ans = 0;
  for (int i = 0; i < n - 1; i++) {
    ans += std::abs(a[i + 1] - a[i]);
  }
  auto work = [&](int x) {
    int min = std::min(std::abs(x - a[0]), std::abs(x - a[n - 1]));
    for (int i = 0; i < n - 1; i++) {
      int now = std::abs(x - a[i]);
      now = std::min(now, std::abs(x - a[i + 1]));
      min = std::min(min, now * 2);
    }
    return min;
  };
  if (*mn != 1) ans += work(1);
  if (*mx < x) ans += work(x);
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
