#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> ans(n + 1);
  int v = 2, d = 2;
  for (int i = 2; i + 1 <= n; i += 3) {
    if (v + d > n || v > n) {
      if (d == 6) break;
      v = 3, d = 6;
      if (v > n || v + d > n) break;
    }
    ans[i] = v;
    ans[i + 1] = v + d;
    v += 2 * d;
  }
  std::vector<int> vis(n + 1);
  for (int i = 1; i <= n; i++) {
    vis[ans[i]] = 1;
  }
  int tot = 1;
  for (int i = 1; i <= n; i++) {
    if (ans[i] == 0) {
      while (tot <= n && vis[tot]) tot++;
      ans[i] = tot;
      vis[tot] = 1;
    }
  }
  for (int i = 1; i <= n; i++) {
    std::cout << ans[i] << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
