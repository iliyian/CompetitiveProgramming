#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, m;
  std::cin >> n >> m;
  std::vector<int> a(n - 1), b(n);
  for (auto &i : a) std::cin >> i;
  a.push_back(1);
  for (auto &i : b) std::cin >> i;
  std::ranges::sort(a);
  std::ranges::sort(b);
  int ans = 0, p = 0;
  for (int i = 0; i < n; i++) {
    auto q = std::upper_bound(b.begin() + p, b.end(), a[i]) - b.begin();
    ans += q - p;
    n -= q - p;
    p = q + 1;
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
