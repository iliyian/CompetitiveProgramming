#include <bits/stdc++.h>
#define int long long

constexpr int mod = 998244353;

int qpow(int a, int b) {
  int ans = 1;
  while (b) {
    if (b & 1) ans = ans * a % mod;
    a = a * a % mod;
    b >>= 1;
  }
  return ans;
}

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n), b(n);
  for (auto &i : a) std::cin >> i;
  for (auto &i : b) std::cin >> i;
  std::map<int, int> mp;
  for (int i = 0; i < n; i++) {
    mp[a[i]] = b[i];
  }
  int m;
  std::cin >> m;
  std::vector<int> c(m), d(m);
  for (auto &i : c) std::cin >> i;
  for (auto &i : d) std::cin >> i;
  int cnt = mp.size();
  for (int i = 0; i < m; i++) {
    if (!mp.count(c[i]) || mp[c[i]] < d[i]) {
      std::cout << 0 << '\n';
      return;
    }
    if (mp[c[i]] == d[i]) {
      cnt--;
    }
  }
  std::cout << qpow(2, cnt) << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
