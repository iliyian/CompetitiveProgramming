#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n), cnt(n);
  for (int i = 0; i < n; i++) {
    std::cin >> a[i];
    if (a[i] < n) {
      cnt[a[i]]++;
    }
  }
  int mex = 0;
  while (mex < n && cnt[mex]) mex++;
  std::vector<int> minf(mex + 1, LLONG_MAX / 3);
  minf[0] = 0;
  for (int i = 1; i <= mex; i++) {
    for (int j = 0; j < i; j++) {
      minf[i] = std::min(minf[i], i * (cnt[j] - 1) + j + minf[j]);
    }
  }
  std::cout << minf[mex] << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
