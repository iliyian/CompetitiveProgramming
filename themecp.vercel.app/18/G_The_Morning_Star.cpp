#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> x(n + 1), y(n + 1), p(n + 1), q(n + 1);
  int ans = 0;
  std::map<int, int> mx, my, mp, mq;
  for (int i = 1; i <= n; i++) {
    std::cin >> x[i] >> y[i];
    p[i] = x[i] - y[i];
    q[i] = x[i] + y[i];
    mx[x[i]]++, my[y[i]]++, mp[p[i]]++, mq[q[i]]++;
  }
  auto add = [&](std::map<int, int> &mp) {
    int ans = 0;
    for (auto [k, v] : mp) {
      ans += v * (v - 1);
      // std::cerr << v << ' ';
    }
    return ans;
  };
  std::cout << add(mx) + add(my) + add(mp) + add(mq) << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
