#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, k;
  std::cin >> n >> k;
  std::vector<int> a(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  int ans = LLONG_MAX / 3;
  for (int v = 0; v <= a[1]; v++) {
    int now = 0;
    for (int i = 1; i <= n; i++) {
      int p = v ? std::min(k, a[i] / v) : k;
      now = std::max(now, a[i] / p - v);
    }
    ans = std::min(ans, now);
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
