#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> l(n + 1), r(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> l[i] >> r[i];
  }
  std::vector<std::pair<int, int>> v;
  std::vector<int> vec = {LLONG_MIN / 3};
  for (int i = 1; i <= n; i++) {
    for (int j = i + 1; j <= n; j++) {
      if (l[i] >= l[j] && l[i] <= r[j] || l[j] >= l[i] && l[j] <= r[i]) {
        v.push_back({std::max(r[i], r[j]), std::min(l[i], l[j])});
      }
    }
  }
  std::ranges::sort(v);
  int ans = 0, prv = -1;
  for (auto &[r, l] : v) {
    if (l > prv) {
      prv = r;
      ans++;
    }
  }
  std::cout << n - ans * 2 << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
