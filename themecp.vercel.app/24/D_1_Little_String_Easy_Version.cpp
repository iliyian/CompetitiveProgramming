#include <bits/stdc++.h>
#define int long long

constexpr int mod = 1e9 + 7;

void solve() {
  int n, k;
  std::cin >> n >> k;
  std::string w;
  std::cin >> w;
  int ans = 1, ansk = 1;
  if (w.front() == '0' || w.back() == '0') {
    std::cout << -1 << '\n';
    return;
  }
  for (int i = 1; i < n; i++) {
    if (w[i] == '1') {
      ans = ans * 2 % mod;
      ansk = ansk * 2 % k;
    } else {
      ans = ans * i % mod;
      ansk = ansk * i % k;
    }
  }
  if (ansk == 0) {
    std::cout << -1 << '\n';
  } else {
    std::cout << ans << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
