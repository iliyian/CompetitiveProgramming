#include <bits/stdc++.h>
#define int long long

using std::numbers::pi;

void print(long double deg) {
  std::cout << deg / pi * 180 << '\n';
}

void solve() {
  int n, r;
  std::cin >> n >> r;
  long double theta = pi / (n * 2);
  long double d = r * sinl(2 * theta) / sinl(theta);
  long double e = d * sinl(theta) / sinl(3 * theta);
  long double S = 0.5l * e * r * sinl(theta);
  long double ans = S * 2 * n;
  std::cout << std::fixed << std::setprecision(20) << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
