#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::string s, t;
  std::cin >> s >> t;
  int pre = 0, ans = 0;
  for (int i = 0; i < n; i++) {
    if ((s[i] - 'a' + pre) % 26 != t[i] - 'a') {
      int temp = (t[i] - s[i] + 26) % 26;
      pre += (temp % 26 - pre % 26) % 26;
      ans++;
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
