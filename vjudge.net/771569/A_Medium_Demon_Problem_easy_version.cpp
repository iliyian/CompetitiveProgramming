#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1), in(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    in[a[i]]++;
  }
  std::vector<int> dis(n + 1);
  std::queue<int> q;
  for (int i = 1; i <= n; i++) {
    if (!in[i]) {
      q.push(i);
    }
  }
  while (!q.empty()) {
    int u = q.front(); q.pop();
    dis[a[u]] = std::max(dis[a[u]], dis[u] + 1);
    if (!--in[a[u]]) {
      q.push(a[u]);
    }
  }
  int ans = 0;
  for (int i = 1; i <= n; i++) {
    if (in[i]) {
      ans = std::max(ans, dis[i] + 2);
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
