#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::string a, b;
  std::cin >> a >> b;
  std::set<std::pair<int, int>> s;
  int cnt = 0;
  for (int i = 0; i < n; i++) {
    auto x = std::pair{a[i], b[n - i - 1]};
    if (x.first > x.second) std::swap(x.first, x.second);
    if (s.count(x)) {
      s.erase(x);
    } else {
      s.insert(x);
    }
  }
  for (auto [x, y] : s) {
    if (x != y) {
      std::cout << "NO\n";
      return;
    }
    cnt++;
  }
  std::cout << (cnt <= 1 ? "YES" : "NO") << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
