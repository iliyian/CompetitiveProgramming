#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, k;
  std::cin >> n >> k;
  if (n > (k - 1) && (n - (k - 1)) % 2 == 1) {
    std::cout << "YES\n";
    std::cout << n - (k - 1) << ' ';
    for (int i = 2; i <= k; i++) {
      std::cout << 1 << ' ';
    }
    std::cout << '\n';
  } else if (n > (k - 1) * 2 && (n - (k - 1) * 2) % 2 == 0) {
    std::cout << "YES\n";
    std::cout << n - (k - 1) * 2 << ' ';
    for (int i = 2; i <= k; i++) {
      std::cout << 2 << ' ';
    }
    std::cout << '\n';
  } else {
    std::cout << "NO\n";
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
