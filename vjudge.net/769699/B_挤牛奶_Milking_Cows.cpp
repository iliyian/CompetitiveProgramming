#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(1000005);
  for (int i = 1; i <= n; i++) {
    int l, r;
    std::cin >> l >> r;
    l++, r++;
    a[l]++, a[r]--;
  }
  int pre = 0, ans0 = 0, ans1 = 0;
  for (int i = 1; i <= 1000004; i++) {
    a[i] += a[i - 1];
    if (a[i] > 0 && a[i - 1] == 0) {
      if (pre) {
        ans0 = std::max(ans0, i - pre);
      }
      pre = i;
    } else if (a[i] == 0 && a[i - 1] > 0) {
      if (pre) {
        ans1 = std::max(ans1, i - pre);
      }
      pre = i;
    }
  }
  std::cout << ans1 << ' ' << ans0 << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
