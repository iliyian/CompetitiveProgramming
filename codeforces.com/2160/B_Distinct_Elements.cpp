#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1), ans(n + 1);
  int tot = 0;
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    int d = a[i] - a[i - 1];
    if (d == i) {
      ans[i] = ++tot;
    } else {
      ans[i] = ans[i - d];
    }
    std::cout << ans[i] << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
