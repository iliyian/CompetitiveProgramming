#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  auto check = [&](int x) {
    for (int i = 0; i <= (x - 1 - i); i++) {
      if (i == x - 1 - i) {
        if (n >> i & 1) {
          return false;
        } else {
          return true;
        }
      }
      if ((n >> i & 1) != (n >> (x - 1 - i) & 1)) {
        // if (x == 5 && n == 10) {
        //   std::cerr << i << '\n';
        // }
        return false;
      }
    }
    return true;
  };
  for (int i = 1; i < 60; i++) {
    if (n <= (1ll << i) - 1) {
      if (check(i)) {
        std::cout << "YES\n";
        return;
      }
    }
  }
  std::cout << "NO\n";
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
