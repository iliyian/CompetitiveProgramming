#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, m, x;
  std::cin >> n >> m >> x;
  std::set<int> s;
  s.insert(x);
  for (int i = 1; i <= m; i++) {
    int d;
    char ch;
    std::cin >> d >> ch;
    std::set<int> t;
    for (auto x : s) {
      if (ch == '?' || ch == '0') {
        int y = x + d;
        if (y > n) y -= n;
        t.insert(y);
      }
      if (ch == '?' || ch == '1') {
        int y = x - d;
        if (y < 1) y += n;
        t.insert(y);
      }
    }
    s = std::move(t);
  }
  std::cout << s.size() << '\n';
  for (auto i : s) {
    std::cout << i << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
