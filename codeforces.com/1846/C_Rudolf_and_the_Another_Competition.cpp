#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, m, h;
  std::cin >> n >> m >> h;
  std::vector<std::vector<int>> a(n, std::vector<int>(m));
  std::vector<std::pair<int, int>> b(n);
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < m; j++) {
      std::cin >> a[i][j];
    }
    std::ranges::sort(a[i]);
    int s = 0, sum = 0, c = 0;
    for (int j = 0; j < m; j++) {
      if (s + a[i][j] > h) break;
      s += a[i][j];
      c++;
      sum += s;
    }
    b[i] = {c, -sum};
    // std::cerr << c << ' ' << -sum << '\n';
  }
  std::cout << std::count_if(b.begin() + 1, b.end(), [&](auto x) {return x > b[0];}) + 1 << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
