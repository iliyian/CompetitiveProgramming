#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n), b(n);
  int cnt[2] {};
  for (int i = 0; i < n; i++) {
    std::cin >> a[i];
    cnt[a[i] & 1]++;
  }
  for (int i = 0; i < n; i++) {
    std::cin >> b[i];
  }
  if (cnt[0] >= 2) {
    std::cout << 0 << '\n';
  } else if (cnt[0] >= 1) {
    std::cout << 1 << '\n';
  } else {
    std::cout << 2 << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
