#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<std::vector<int>> g(n + 1);
  std::vector<int> dep(n + 1), pa(n + 1);
  for (int i = 2; i <= n; i++) {
    int x, y;
    std::cin >> x >> y;
    g[x].push_back(y);
    g[y].push_back(x);
  }
  std::vector<int> in(n + 1);
  [&](this auto &&self, int u, int p) -> void {
    dep[u] = dep[p] ^ 1;
    pa[u] = p;
    in[p]++;
    for (int v : g[u]) {
      if (v != p) {
        self(v, u);
      }
    }
  } (n, 0);
  std::queue<int> q;
  for (int i = 1; i <= n; i++) {
    if (!in[i]) {
      q.push(i);
    }
  }
  std::cout << n * 3 - 3 << '\n';
  int cur = dep[1];
  while (!q.empty()) {
    int u = q.front(); q.pop();
    if (u == n) continue;
    if (dep[u] != dep[cur]) {
      std::cout << 1 << '\n';
      std::cout << 1 << '\n';
      std::cout << 2 << ' ' << u << '\n';
    } else {
      std::cout << 1 << '\n';
      std::cout << 2 << ' ' << u << '\n';
      std::cout << 1 << '\n';
    }
    if (!--in[pa[u]]) {
      q.push(pa[u]);
    }
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
