#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  for (int i = 0; i < n; i++) {
    std::cin >> a[i];
  }
  int mx = a[0], ans = 0;
  for (int i = 1; i < n; i++) {
    mx = std::max(mx, a[i]);
    if (i % 2 == 1) {
      a[i] = mx;
    } else if (a[i] >= a[i - 1]) {
      ans += a[i] - a[i - 1] + 1;
    }
  }
  if (a[0] >= a[1]) {
    ans += a[0] - a[1] + 1;
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
