#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, h, l;
  std::cin >> n >> h >> l;
  int ans = 0, tot = 0;
  for (int i = 1; i <= n; i++) {
    int x;
    std::cin >> x;
    ans += (x <= std::min(h, l));
    tot += (x <= std::max(h, l));
  }
  std::cout << std::min(ans, tot / 2) << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
