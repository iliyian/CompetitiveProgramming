#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, x;
  std::cin >> n >> x;
  std::vector<int> a(n + 1), b(n + 1), c(n + 1), v(n + 1);
  int now = 0, mx = 0;
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i] >> b[i] >> c[i];
    now += a[i] * (b[i] - 1);
    mx = std::max(mx, -c[i] + a[i] * b[i]);
  }
  if (now >= x) {
    std::cout << 0 << '\n';
  } else if (mx == 0) {
    std::cout << -1 << '\n';
  } else {
    std::cout << (x - now + mx - 1) / mx << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
