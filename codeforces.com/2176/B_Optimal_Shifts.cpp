#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::string s;
  std::cin >> n >> s;
  std::vector<int> p;
  for (int i = 0; i < n; i++) {
    if (s[i] == '1') {
      p.push_back(i);
    }
  }
  int ans = 0;
  for (int i = 0; i < p.size() - 1; i++) {
    ans = std::max(ans, p[i + 1] - p[i] - 1);
  }
  ans = std::max(ans, p[0] + n - p.back() - 1);
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
