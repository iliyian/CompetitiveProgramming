#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1), pos(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    pos[a[i]] = i;
  }
  int pre = n + 1, mx = 0;
  std::vector<std::pair<int, int>> ans;
  for (int i = 1; i <= n; i++) {
    if (pos[i] < pre) {
      if (i != 1) {
        if (i > mx) {
          std::cout << "No\n";
          return;
        }
        ans.push_back({i, mx});
      }
      for (int j = pos[i] + 1; j < pre; j++) {
        ans.push_back({i, a[j]});
        mx = std::max(mx, a[j]);
      }
      pre = pos[i];
    }
  }
  std::cout << "Yes\n";
  for (auto [x, y] : ans) {
    std::cout << x << ' ' << y << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
