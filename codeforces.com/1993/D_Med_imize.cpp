#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, k;
  std::cin >> n >> k;
  std::vector<int> a(n);
  for (auto &i : a) std::cin >> i;
  int len = (n - 1) % k + 1;
  auto check = [&](int mid) -> bool {
    std::vector<int> f(n);
    f[0] = (a[0] >= mid);
    int mx = 0;
    for (int i = 1; i < n; i++) {
      if (i % k == 0) {
        f[i] = std::max(f[i - k], (int)(a[i] >= mid));
      } else {
        f[i] = f[i - 1] + (a[i] >= mid);
        if (i >= k) {
          f[i] = std::max(f[i], f[i - k]);
        }
      }
      if (i % k == len - 1) {
        mx = std::max(mx, f[i]);
      }
    }
    if (len == 1) {
      mx = std::max(mx, f[0]);
    }
    return mx > len / 2;
  };
  int l = 1, r = 1e9, ans = -1;
  while (l <= r) {
    int mid = (l + r) / 2;
    if (check(mid)) l = mid + 1, ans = mid;
    else r = mid - 1;
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
