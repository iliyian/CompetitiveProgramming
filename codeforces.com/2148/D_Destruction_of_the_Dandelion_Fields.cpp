#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  std::vector<std::vector<int>> b(2);
  for (int i = 0; i < n; i++) {
    std::cin >> a[i];
    b[a[i] % 2].push_back(a[i]);
  }
  int ans = 0;
  if (b[1].empty()) {
    std::cout << 0 << '\n';
    return;
  }
  for (auto z : {0, 1}) {
    if (z == 0) {
      ans += std::accumulate(b[z].begin(), b[z].end(), 0ll);
    } else {
      std::ranges::sort(b[z], std::greater<>());
      ans += std::accumulate(b[z].begin(), b[z].begin() + (b[z].size() + 1) / 2, 0ll);
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
