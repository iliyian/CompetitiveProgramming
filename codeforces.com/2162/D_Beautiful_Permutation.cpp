#include <bits/stdc++.h>
#define int long long

int ask(int op, int l, int r) {
  std::cout << op << ' ' << l << ' ' << r << std::endl;
  int ans;
  std::cin >> ans;
  return ans;
}

void solve() {
  int n;
  std::cin >> n;
  int l = 1, r = n;
  int len = ask(2, 1, n) - ask(1, 1, n), R = -1;
  while (l <= r) {
    int mid = (l + r) / 2;
    int ans1 = ask(1, 1, mid), ans2 = ask(2, 1, mid);
    if (ans2 - ans1 >= len) r = mid - 1, R = mid;
    else l = mid + 1;
  }
  std::cout << "! " << R - len + 1 << ' ' << R << std::endl;
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
