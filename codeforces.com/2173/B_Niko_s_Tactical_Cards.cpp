#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1), b(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  for (int i = 1; i <= n; i++) {
    std::cin >> b[i];
  }
  std::vector<int> mx(n + 1, LLONG_MIN / 3), mn(n + 1, LLONG_MAX / 3);
  mx[0] = mn[0] = 0;
  for (int i = 1; i <= n; i++) {
    mx[i] = std::max({mx[i - 1] - a[i], mn[i - 1] - a[i], b[i] - mx[i - 1], b[i] - mn[i - 1]});
    mn[i] = std::min({mx[i - 1] - a[i], mn[i - 1] - a[i], b[i] - mx[i - 1], b[i] - mn[i - 1]});
  }
  std::cout << mx[n] << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
