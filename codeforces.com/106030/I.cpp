#include <bits/stdc++.h>
#define int long long
using namespace std;
constexpr int mod = 998244353;

int qpow(int a, int b) {
  int ans = 1;
  while (b) {
    if (b & 1) ans = ans * a % mod;
    a = a * a % mod;
    b >>= 1;
  }
  return ans;
}

void solve() {
  int ans = 1;
  std::array<int, 11> a{};
  a[10] = 0;
  for (int i = 1; i <= 9; i++) {
    std::cin >> a[i];
  }
  int t = min(a[1],a[2]);
  a[3] += t; a[1] -= t; a[2] -= t;
  t = a[1] / 3;
  a[3] += t;
  a[1] -= t * 3;
  if(a[1] == 2) {a[1] = 0; a[2]++;}
  else if(a[1] == 1) {
    for(int i = 2; i <= 10; i++) {
      if(a[i]) {
        a[i+1]++;
        a[i]--;
        break;
      }
    }
  }
  for(int i = 2; i <= 10; i++) ans = ans * qpow(i,a[i]) % mod;
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
