#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, m;
  std::cin >> n >> m;
  std::vector<int> a(n + 1), in(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  if (n == 1) {
    std::cout << a[1] << '\n';
    return;
  }
  int mx = 0, mx1 = 0, mx2 = 0;
  for (int i = 1; i <= m; i++) {
    int x, y;
    std::cin >> x >> y;
    in[x]++, in[y]++;
  }
  for (int i = 1; i <= n; i++) {
    if (in[i] == 1) {
      if (a[i] > mx1) {
        mx2 = mx1;
        mx1 = a[i];
      } else if (a[i] > mx2) {
        mx2 = a[i];
      }
    } else if (in[i] >= 2) {
      mx = std::max(mx, a[i]);
    }
  }
  std::cout << std::max(mx, mx2) << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
