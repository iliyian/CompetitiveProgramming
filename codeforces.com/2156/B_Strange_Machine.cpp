#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, q;
  std::cin >> n >> q;
  std::string s;
  std::cin >> s;
  bool flag = std::count(s.begin(), s.end(), 'B');
  while (q--) {
    int x;
    std::cin >> x;
    if (!flag) {
      std::cout << x << '\n';
    } else {
      int y = x;
      for (int i = 0;; i++) {
        if (s[i % n] == 'B') y /= 2;
        else y--;
        if (y == 0) {
          std::cout << i + 1 << '\n';
          break;
        }
      }
    }
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
