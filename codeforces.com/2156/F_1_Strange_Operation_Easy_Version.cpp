#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n), pos(n);
  for (auto &i : a) std::cin >> i, --i;
  for (int i = 0; i < n; i++) {
    pos[a[i]] = i;
  }
  while (1) {
    bool flag = false;
    for (int x = 2; x < n; x++) {
      int i = pos[x], j = pos[x - 1], k = pos[x - 2];
      int y = x - 1, z = x - 2;
      if (i < j && i < k) {
        pos[x] = j;
        pos[y] = k;
        pos[z] = i;
        a[i] = z;
        a[j] = x;
        a[k] = y;
        flag = true;
      }
    }
    if (!flag) {
      break;
    }
  }
  for (int i = 0; i < n; i++) {
    std::cout << a[i] + 1 << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
