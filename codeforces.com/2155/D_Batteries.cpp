#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  for (int d = 1;; d++) {
    for (int i = 1; i + d <= n; i++) {
      std::cout << i << ' ' << i + d << std::endl;
      int ans;
      std::cin >> ans;
      if (ans) {
        return;
      }
    }
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
