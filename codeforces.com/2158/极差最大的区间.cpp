#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  for (auto &i : a) std::cin >> i;
  int ansl = 0, ansr = 0, ans = -1;
  for (int l = 0; l < n; l++) {
    for (int r = l; r < n; r++) {
      int mx = LLONG_MIN, mn = LLONG_MAX;
      for (int i = l; i <= r; i++) {
        mx = std::max(mx, a[i]);
        mn = std::min(mn, a[i]);
      }
      if (mx - mn > ans) {
        ans = mx - mn;
        ansl = l + 1, ansr = r + 1;
      }
    }
  }
  std::cout << ansl << ' ' << ansr << ' ' << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
