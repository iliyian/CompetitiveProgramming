#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 1), d;
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    if (i > 1) {
      d.push_back(a[i] - a[i - 1]);
    }
  }
  std::ranges::sort(d, std::greater<>());
  int ans = a[n] - a[1];
  std::cout << ans << ' ';
  for (int i = 2; i <= n; i++) {
    ans -= d[i - 2];
    std::cout << ans << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
