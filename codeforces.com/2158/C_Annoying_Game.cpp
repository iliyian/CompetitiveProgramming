#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, k;
  std::cin >> n >> k;
  std::vector<std::array<int, 2>> f(n + 1, {LLONG_MIN / 3, LLONG_MIN / 3});
  std::vector<int> a(n + 1), b(n + 1);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  for (int i = 1; i <= n; i++) {
    std::cin >> b[i];
  }
  f[0][0] = 0;
  int mx[2] {LLONG_MIN, LLONG_MIN};
  for (int i = 1; i <= n; i++) {
    f[i][0] = std::max(a[i], f[i - 1][0] + a[i]);
    f[i][1] = std::max({a[i] + b[i], f[i - 1][0] + a[i] + b[i], f[i - 1][1] + a[i]});
    mx[0] = std::max(mx[0], f[i][0]);
    mx[1] = std::max(mx[1], f[i][1]);
  }
  std::cout << mx[k % 2] << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
