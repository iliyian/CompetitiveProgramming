#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::map<int, int> cnt;
  for (int i = 1; i <= n * 2; i++) {
    int x;
    std::cin >> x;
    cnt[x]++;
  }
  int ans = 0, c = 0, c1 = 0;
  for (auto [k, v] : cnt) {
    if (v % 2 == 1) {
      ans += 1;
      c1++;
    } else {
      int vv = v / 2;
      if (vv % 2 == 1) ans += 2;
      else c++;
    }
  }
  std::cout << ans + (c1 ? c * 2 : c / 2 * 4) << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
