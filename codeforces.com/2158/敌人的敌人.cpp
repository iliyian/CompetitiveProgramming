#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> cnt(n + 1);
  std::vector<std::vector<int>> g(n + 1);
  for (int i = 2; i <= n; i++) {
    int x, y;
    std::cin >> x >> y;
    cnt[x]++, cnt[y]++;
    g[x].push_back(y);
    g[y].push_back(x);
  }
  int ans = 0, which = 1;
  for (int i = 1; i <= n; i++) {
    int sum = 0;
    for (int v : g[i]) {
      sum += cnt[v] - 1;
    }
    if (sum > ans) {
      ans = sum;
      which = i;
    }
  }
  std::cout << which << ' ' << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
