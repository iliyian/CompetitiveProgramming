#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  for (auto &i : a) std::cin >> i;
  if (std::ranges::count_if(a, [&](int x) {return x % 2;}) && std::ranges::count_if(a, [&](int x) {return x % 2 == 0;})) {
    std::ranges::sort(a);
  }
  for (auto i : a) {
    std::cout << i << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
