#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, k;
  std::cin >> n >> k;
  std::vector<std::vector<int>> g(n);
  for (int i = 0; i < n - 1; i++) {
    int x, y;
    std::cin >> x >> y;
    x--, y--;
    g[x].push_back(y);
    g[y].push_back(x);
  }
  int ans = 0;
  std::vector<int> siz(n);
  [&](this auto &&self, int u, int p) -> void {
    siz[u] = 1;
    for (int v : g[u]) {
      if (v != p) {
        self(v, u);
        siz[u] += siz[v];
        // if (n - siz[v] >= k) {
        //   ans += siz[v];
        // }
      }
    };
    if (siz[u] >= k) {
      ans += n - siz[u];
    }
    if (n - siz[u] >= k) {
      ans += siz[u];
    }
  } (0, -1);
  std::cout << ans + n << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
