#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n + 2);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  int ans = 0;
  int l = 0, r = a[2] - a[1];
  a[n + 1] = LLONG_MAX / 3;
  // std::vector<std::array<int, 2>> f(n + 1, {LLONG_MIN / 3, LLONG_MIN / 3});
  for (int i = 2; i <= n; i++) {
    int mxr = std::min(a[i + 1] - a[i], a[i] - a[i - 1]);
    int d = a[i] - a[i - 1];
    // (0, mxr)
    int ll = d - r, rr = d - l;
    if (ll < mxr) {
      ans++;
      l = ll, r = std::min(rr, mxr);
    } else {
      l = 0, r = mxr;
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
