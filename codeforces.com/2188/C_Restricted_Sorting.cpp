#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  for (auto &i : a) std::cin >> i;
  auto b = a;
  std::ranges::sort(b);
  int mn = LLONG_MAX / 3;
  for (int i = 0; i < n; i++) {
    if (a[i] != b[i]) {
      mn = std::min(mn, std::max(a[i] - b[0], b[n - 1] - a[i]));
    }
  }
  if (mn == LLONG_MAX / 3) mn = -1;
  std::cout << mn << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
