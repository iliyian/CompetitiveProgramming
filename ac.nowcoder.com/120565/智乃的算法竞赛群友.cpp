#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, a, b;
  std::cin >> n >> a >> b;
  int ans = 0;
  auto work = [&](int w1, int v1, int w2, int v2) {
    for (int x = 0; x * w1 <= n && x <= 500; x++) {
      int y = (n - x * w1) / w2;
      ans = std::max(ans, x * v1 + y * v2);
    }
    for (int y = 0; y * w2 <= n && y <= 500; y++) {
      int x = (n - y * w2) / w1;
      ans = std::max(ans, x * v1 + y * v2);
    }
  };
  work(8, a + b, 7, a);
  work(8, a + b, 2, b);
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
