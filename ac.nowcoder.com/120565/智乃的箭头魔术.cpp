#include <bits/stdc++.h>
#define int long long

void solve() {
  // std::string s = "012345";
  std::string s = "0112233445142015320125410214530214510214102302142025101203201451451522302514203214510021454101002532";
  int now = 0;
  for (char c : s) {
    if (c == '0') {
      now = 3 - now;
    }
    if (c == '1') {
      if (now % 2 == 1) {
        now = 4 - now;
      }
    }
    if (c == '2') {
      now = now ^ 1;
    }
    if (c == '3') {
      if (now % 2 == 0) {
        now = 2 - now;
      }
    }
    if (c == '4') {
      now = (now + 1) % 4;
    }
    if (c == '5') {
      now = (now + 3) % 4;
    }
    std::cout << now;
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
