#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  std::map<int, int> lstpos;
  for (int &i : a) std::cin >> i;
  int ans = 0, sumlstpos = 0, sumpos = 0;
  auto upd = [&](int val, int idx) {
    sumlstpos -= lstpos[val];
    lstpos[val] = idx;
    sumlstpos += lstpos[val];
    sumpos += idx;
  };
  int cur = 0;
  for (int i = 0; i < n; i++) {
    upd(a[i], i + 1);
    cur += (i + 1) * (i + 2) / 2 - (sumpos - sumlstpos);
    ans += cur;
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
