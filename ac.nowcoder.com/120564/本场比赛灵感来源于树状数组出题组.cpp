#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  for (auto &i : a) std::cin >> i;
  int ans = 0;
  for (auto i : a) {
    int cnt = -1;
    for (auto j : a) {
      cnt += j <= i;
    }
    // std::cerr << cnt << '\n';
    if (cnt * 10 >= (n - 1) * 8) {
      ans += i;
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
