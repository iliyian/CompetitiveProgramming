#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, q, s;
  std::cin >> n >> q >> s;
  std::vector<int> a(n + 1);
  a[0] = s;
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    a[i] += a[i - 1];
  }
  while (q--) {
    int x, y;
    std::cin >> x >> y;
    std::cout << a[x - 1] + y - 1 << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
