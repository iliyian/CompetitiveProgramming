#include <bits/stdc++.h>
#define int long long

void solve() {
  int x1, y1, x2, y2;
  std::cin >> x1 >> y1 >> x2 >> y2;
  if (y1 == y2) {
    if (std::abs(x1 - x2) * std::abs(y1) != 4) {
      std::cout << "no answer\n";
      return;
    }
    std::cout << 0 << '\n';
    return;
  }
  auto cal = [&](double x1, double y1, double x2, double y2) {
    return x1 * y2 - x2 * y1;
  };
  long double ans = 1.0l * (x1 * y2 - x2 * y1 - 4) / (y2 - y1);
  std::cout << std::fixed << std::setprecision(20) << ans << '\n';
  // std::cout << cal(x1 - ans, y1, x2 - ans, y2) << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
