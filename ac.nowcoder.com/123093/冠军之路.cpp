#include <bits/stdc++.h>
#define int long long

struct HLD {
    int n;
    std::vector<int> siz, top, dep, parent, in, out, seq;
    std::vector<std::vector<int>> adj;
    int cur;
    
    HLD() {}
    HLD(int n) {
        init(n);
    }
    void init(int n) {
        this->n = n;
        siz.resize(n);
        top.resize(n);
        dep.resize(n);
        parent.resize(n);
        in.resize(n);
        out.resize(n);
        seq.resize(n);
        cur = 0;
        adj.assign(n, {});
    }
    void addEdge(int u, int v) {
        adj[u].push_back(v);
        adj[v].push_back(u);
    }
    void work(int root = 0) {
        top[root] = root;
        dep[root] = 0;
        parent[root] = -1;
        dfs1(root);
        dfs2(root);
    }
    void dfs1(int u) {
        if (parent[u] != -1) {
            adj[u].erase(std::find(adj[u].begin(), adj[u].end(), parent[u]));
        }
        
        siz[u] = 1;
        for (auto &v : adj[u]) {
            parent[v] = u;
            dep[v] = dep[u] + 1;
            dfs1(v);
            siz[u] += siz[v];
            if (siz[v] > siz[adj[u][0]]) {
                std::swap(v, adj[u][0]);
            }
        }
    }
    void dfs2(int u) {
        in[u] = cur++;
        seq[in[u]] = u;
        for (auto v : adj[u]) {
            top[v] = v == adj[u][0] ? top[u] : v;
            dfs2(v);
        }
        out[u] = cur;
    }
    int lca(int u, int v) {
        while (top[u] != top[v]) {
            if (dep[top[u]] > dep[top[v]]) {
                u = parent[top[u]];
            } else {
                v = parent[top[v]];
            }
        }
        return dep[u] < dep[v] ? u : v;
    }
    
    int dist(int u, int v) {
        return dep[u] + dep[v] - 2 * dep[lca(u, v)];
    }
    
    int jump(int u, int k) {
        if (dep[u] < k) {
            return -1;
        }
        
        int d = dep[u] - k;
        
        while (dep[top[u]] > d) {
            u = parent[top[u]];
        }
        
        return seq[in[u] - dep[u] + d];
    }
    
    bool isAncester(int u, int v) {
        return in[u] <= in[v] && in[v] < out[u];
    }
    
    int rootedParent(int u, int v) {
        std::swap(u, v);
        if (u == v) {
            return u;
        }
        if (!isAncester(u, v)) {
            return parent[u];
        }
        auto it = std::upper_bound(adj[u].begin(), adj[u].end(), v, [&](int x, int y) {
            return in[x] < in[y];
        }) - 1;
        return *it;
    }
    
    int rootedSize(int u, int v) {
        if (u == v) {
            return n;
        }
        if (!isAncester(v, u)) {
            return siz[v];
        }
        return n - siz[rootedParent(u, v)];
    }
    
    int rootedLca(int a, int b, int c) {
        return lca(a, b) ^ lca(b, c) ^ lca(c, a);
    }
};

void solve() {
  int n, m, q;
  std::cin >> n >> m >> q;
  HLD hld(n + 1);
  for (int i = 2; i <= n; i++) {
    int x, y;
    std::cin >> x >> y;
    hld.addEdge(x, y);
  }
  std::vector<int> dis(n + 1, LLONG_MAX / 3);
  std::queue<std::pair<int, int>> que;
  for (int i = 1; i <= m; i++) {
    int x;
    std::cin >> x;
    que.push({dis[x] = 0, x});
  }
  while (!que.empty()) {
    auto [d, u] = que.front(); que.pop();
    for (int v : hld.adj[u]) {
      if (dis[v] > dis[u] + 1) {
        que.push({dis[v] = dis[u] + 1, v});
      }
    }
  }
  hld.work(1);
  while (q--) {
    int x, y;
    std::cin >> x >> y;
    int d = hld.dist(x, y), lca = hld.lca(x, y);
    if (d > dis[y]) {
      std::cout << -1 << '\n';
      continue;
    }
    if (d < dis[y]) {
      std::cout << 0 << '\n';
      continue;
    }
    int dx = hld.dist(x, lca);
    auto check = [&](int mid) -> bool {
      int u, k;
      if (mid >= dx) {
        k = d - mid;
        u = y;
      } else {
        k = mid;
        u = x;
      }
      u = hld.jump(u, k);
      return dis[u] == mid;
    };
    int l = 0, r = d, ans = -1;
    while (l <= r) {
      int mid = (l + r) / 2;
      if (check(mid)) r = mid - 1, ans = mid;
      else l = mid + 1;
    }
    std::cout << d - ans << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
