#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<std::string> a(n + 1);
  int ans = 0;
  auto work = [&](std::string &s) {
    int ans = std::max(ans, std::stoll(s));
    for (int i = 0; i < s.size(); i++) {
      for (int j = i + 1; j < s.size(); j++) {
        std::swap(s[i], s[j]);
        ans = std::max(ans, std::stoll(s));
        std::swap(s[i], s[j]);
      }
    }
    // std::cerr << ans << '\n';
    return ans;
  };
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    ans += work(a[i]);
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
