#include <bits/stdc++.h>
#define int long long

void solve() {
  int k, n;
  std::cin >> k >> n;
  std::vector<int> a(n + 2);
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
  }
  k--;
  int s = a[1];
  for (int i = 2; i <= n; i++) {
    int t = (a[i] - s + a[i - 1] - 1) / a[i - 1];
    t = std::max(0ll, t);
    if (t > k - 1) {
      s += k * a[i - 1];
      k = 0;
      break;
    }
    k -= t + 1;
    s += t * a[i - 1] + a[i];
  }
  s += k * a[n];
  std::cout << s << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
