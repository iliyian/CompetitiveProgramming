#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, x;
  std::cin >> n >> x;
  std::vector<int> a(n + 1), add(n + 2);
  int ad = 0, ans = 0;
  for (int i = 1; i <= n; i++) {
    std::cin >> a[i];
    ad -= add[i];
    a[i] += ad;
    if (a[i] < a[i - 1]) {
      int r = std::min(n + 1, i + x);
      add[r] += a[i - 1] - a[i];
      ad += a[i - 1] - a[i];
      ans += a[i - 1] - a[i];
      a[i] = a[i - 1];
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
