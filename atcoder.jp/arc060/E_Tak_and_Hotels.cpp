#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  for (int i = 0; i < n; i++) {
    std::cin >> a[i];
  }
  int L;
  std::cin >> L;
  const int logn = std::__lg(n);
  std::vector<std::vector<int>> f(logn + 1, std::vector<int>(n));
  for (int i = 0; i < n; i++) {
    f[0][i] = std::ranges::upper_bound(a, a[i] + L) - a.begin() - 1;
  }
  for (int j = 0; j < logn; j++) {
    for (int i = 0; i < n; i++) {
      f[j + 1][i] = f[j][f[j][i]];
    }
  }
  int q;
  std::cin >> q;
  while (q--) {
    int a, b;
    std::cin >> a >> b;
    a--, b--;
    if (a > b) {
      std::swap(a, b);
    }
    int ans = 0;
    for (int j = logn; j >= 0; j--) {
      if (f[j][a] < b) {
        a = f[j][a];
        ans += 1ll << j;
      }
    }
    std::cout << ans + 1 << '\n';
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
