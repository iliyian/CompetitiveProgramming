#include <bits/stdc++.h>
#define int long long

int check(int x) {
  if (x < 10) return x;
  return x % 10 + check(x / 10);
}

void solve() {
  int k;
  std::cin >> k;
  int ans = LLONG_MAX;
  for (int i = 1; i <= k * 200; i++) {
    if (ans > check(i * k)) {
      ans = check(i * k);
      std::cout << i << ' ' << i * k << ' ' << ans << '\n';
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
