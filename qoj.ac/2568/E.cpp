#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n), b(n);
  int sa = 0, sb = 0, ca = 0, cb = 0;
  for (auto &i : a) std::cin >> i, sa = (sa + i) % 2, ca += i % 2;
  for (auto &i : b) std::cin >> i, sb = (sb + i) % 2, cb += i % 2;
  std::ranges::sort(a);
  std::ranges::sort(b);
  if (a == b) {
    std::cout << "Yes\n";
    return;
  }
  bool flag = false, flag2 = false;
  for (int i = 0; i < n - 1; i++) flag |= a[i] == a[i + 1] - 1;
  for (int i = 0; i < n - 1; i++) flag2 |= b[i] == b[i + 1] - 1;
  std::cout << (sa == sb && flag && flag2 && ca == cb ? "Yes" : "No") << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
