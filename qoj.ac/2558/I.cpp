#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<std::vector<int>> a(n + 1, std::vector<int>(n + 1));
  for (int i = 1; i <= n; i++) {
    for (int j = i; j <= n; j++) {
      std::cin >> a[i][j];
    }
  }
  for (int u = 1; u <= n; u++) {
    for (int v = u + 1; v <= n; v++) {
      int x = a[1][u] ^ a[1][v] ^ a[u][v];
      if ((x == u || x == v) && (a[u][v] == (u ^ v))) {
        std::cout << u << ' ' << v << '\n';
      }
    }
  }
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
