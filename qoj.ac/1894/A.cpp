#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n * 3), b(n * 3);
  std::vector<std::array<int, 3>> v;
  for (int i = 0; i < n * 3; i++) {
    std::cin >> a[i] >> b[i];
    v.push_back({b[i], a[i], i});
  }
  std::ranges::sort(v);
  for (int i = 0; i < n * 3; i += 3) {
    int mn = LLONG_MAX, mx = LLONG_MIN;
    for (int j = i; j < i + 3; j++) {
      mn = std::min(mn, v[j][0]);
      mx = std::max(mx, v[j][1]);
    }
    if (mx <= mn) {
    } else {
      std::cout << "No\n";
      return;
    }
  }
  std::cout << "Yes\n";
  for (int i = 0; i < n * 3; i++) {
    std::cout << v[i][2] + 1 << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
