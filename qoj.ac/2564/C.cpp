#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, T;
  std::cin >> n >> T;
  std::vector<int> t(n), k(n);
  for (int i = 0; i < n; i++) {
    std::cin >> t[i] >> k[i];
  }
  int sumt = 0;
  for (int i = 0; i < n; i++) {
    sumt += 1;
    if (sumt > T) {
      if (k[i] < sumt - T) {
        continue;
      } else {
        int dif = k[i] + T - sumt;
        sumt += std::min(t[i] - 1, dif + 1);
        continue;
      }
    }
    sumt += t[i] - 1;
  }
  std::cout << sumt << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
