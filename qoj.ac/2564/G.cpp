#include <bits/stdc++.h>
#define int long long

std::vector<int> primes, minp;

void sieve(int N) {
  minp.assign(N + 1, 0);
  for (int i = 2; i <= N; i++) {
    if (minp[i] == 0) {
      minp[i] = i;
      primes.push_back(i);
    }
    for (auto p : primes) {
      if (i * p > N) break;
      minp[i * p] = p;
      if (i % p == 0) break;
    }
  }
}

void solve() {
  sieve(300000);
  int n, k;
  std::cin >> n >> k;
  if (k - 2 >= primes.size() || primes[k - 2] > n) {
    std::cout << "NO\n";
    return;
  }
  std::cout << "YES\n";
  std::cout << 1 << ' ';
  for (int i = 0; i < k - 1; i++) {
    std::cout << primes[i] << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
