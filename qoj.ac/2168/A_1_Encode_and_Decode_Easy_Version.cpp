#include <bits/stdc++.h>
#define int long long

void solve() {
  std::string op;
  std::cin >> op;
  if (op == "first") {
    int n;
    std::cin >> n;
    for (int i = 0; i < n; i++) {
      int x;
      std::cin >> x;
      std::cout << char(x + 'a' - 1);
    }
  } else {
    std::string s;
    std::cin >> s;
    std::cout << s.size() << '\n';
    for (auto c : s) {
      std::cout << (int)(c - 'a' + 1) << ' ';
    }
  }
}

