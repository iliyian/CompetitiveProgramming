#include <bits/stdc++.h>
using namespace std;
#define int long long
const int MOD = 998244353;
const int MAXN = 2e5 + 7;
// int a[MAXN];
void solve() {
  int n,k;
  cin >> n >> k;
  std::vector<int> a(n + 1);
  for(int i = 1; i <= n; i++) cin >> a[i];
  std::sort(a.begin() + 1, a.end(), std::greater<>());
  int ans = a[1] % MOD;
  int t = 1;
  for (int i = 2; i <= n; i++) {
    t = t * a[i] % MOD;
    if (a[i] == 0) {
      break;
    }
    if ((i - 1) % (k - 1) == 0) {
      ans = ans * t % MOD;
      t = 1;
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);
  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
