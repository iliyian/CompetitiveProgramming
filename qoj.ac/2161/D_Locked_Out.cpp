#include <bits/stdc++.h>
#define int long long

void solve() {
  int n;
  std::cin >> n;
  std::vector<int> a(n);
  std::vector<std::vector<int>> pos(n);
  for (int i = 0; i < n; i++) {
    std::cin >> a[i];
    a[i]--;
    pos[a[i]].push_back(i);
  }
  int ans = 0;
  for (int i = 0; i < n - 1; i++) {
    for (int j = pos[i].size() - 1; j >= 0; j--) {
      if (!pos[i + 1].empty() && pos[i][j] < pos[i + 1].back()) {
        ans++;
        pos[i + 1].pop_back();
      }
    }
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
