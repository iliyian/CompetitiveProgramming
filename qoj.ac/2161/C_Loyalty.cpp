#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, x;
  std::cin >> n >> x;
  std::vector<int> a(n);
  for (auto &i : a) {
    std::cin >> i;
  }
  std::ranges::sort(a);
  int s = 0, ans = 0;
  std::vector<int> v;
  for (int i = 0, j = n - 1; i <= j;) {
    if (s + a[j] >= x) {
      s = (s + a[j]) % x;
      ans += a[j];
      v.push_back(a[j]);
      j--;
    } else {
      s += a[i];
      v.push_back(a[i]);
      if (s >= x) {
        s %= x;
        ans += a[i];
      }
      i++;
    }
  }
  std::cout << ans << '\n';
  for (auto i : v) {
    std::cout << i << ' ';
  }
  std::cout << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
