#include <bits/stdc++.h>
#define int long long

void solve() {
  int r0, x, n, d;
  std::cin >> r0 >> x >> d >> n;
  std::string s;
  std::cin >> s;
  int ans = 0;
  for (auto c : s) {
    if (c == '1') {
      r0 -= d;
      ans++;
    } else {
      if (r0 < x) {
        r0 -= d;
        ans++;
      }
    }
    r0 = std::max(r0, 0ll);
  }
  std::cout << ans << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
