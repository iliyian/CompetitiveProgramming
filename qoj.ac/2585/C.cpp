#include <bits/stdc++.h>
#define int long long

constexpr int mod = 998244353;

int qpow(int a, int b) {
  int ans = 1;
  while (b) {
    if (b & 1) ans = ans * a % mod;
    a = a * a % mod;
    b >>= 1;
  }
  return ans;
}

void solve() {
  int n;
  std::cin >> n;
  std::vector<std::vector<int>> g(n + 1);
  for (int i = 0; i < n - 1; i++) {
    int x, y;
    std::cin >> x >> y;
    g[x].push_back(y);
    g[y].push_back(x);
  }
  int cnt = 0;
  std::vector<int> d(n + 1);
  auto add = [&](int x, int c) -> void {for (int i = x; i <= n; i += i & -i) d[i] += c;};
  auto query = [&](int x) -> int {int ans = 0; for (int i = x; i > 0; i -= i & -i) ans += d[i]; return ans;};
  [&](this auto &&self, int u, int p) -> void {
    cnt += query(u - 1);
    for (int v : g[u]) {
      if (v != p) {
        add(v, 1);
        self(v, u);
        add(v, -1);
      }
    }
  } (1, 0);
  std::cout << qpow(2, cnt) << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  // std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
