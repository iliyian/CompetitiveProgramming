#include <bits/stdc++.h>
#define int long long

void solve() {
  int n, m, k, b;
  std::cin >> n >> m >> k >> b;
  std::vector<std::vector<int>> a(n, std::vector<int>(m));
  std::vector<int> s(n), t(n);
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < m; j++) {
      std::cin >> a[i][j];
      s[i] += a[i][j];
      if (a[i][j] <= 0) {
        t[i]++;
      } else {
        t[i]--;
      }
    }
    t[i] += s[i];
  }
  int ans = 0, cnt = 0;
  for (int i = 0; i < n; i++) {
    if (s[i] >= k) {
      ans++;
    }
    if (s[i] < k && t[i] >= k) {
      cnt++;
    }
  }
  std::cout << ans + std::min(cnt, b) << '\n';
}

int32_t main() {
  std::cin.tie(nullptr)->sync_with_stdio(false);

  int t = 1;
  std::cin >> t;

  while (t--) {
    solve();
  }

  return 0;
}
